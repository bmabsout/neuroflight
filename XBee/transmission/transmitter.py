def listToString(s): 
    # initialize an empty string
    str1 = "" 
    # traverse in the string  
    for ele in s:
        str1 += chr(ele)
    split_strings = []
    n  = 8
    for index in range(0, len(str1), n):
        split_strings.append(str1[index : index + n])

    #print(split_strings)    
    # return string  
    return split_strings 

